# Add more generic sentences to reach 50...
for i in range(20):
    corpus.append(f"Generic informational sentence number {i+1} for dataset scaling.")

# 3. EMBEDDING GENERATION
print("Encoding dataset... please wait.")
corpus_embeddings = model.encode(corpus, convert_to_tensor=True)

# 4. SEARCH FUNCTION
def get_top_k(query, k=5):
    query_embedding = model.encode(query, convert_to_tensor=True)
    # Compute cosine similarity
    cos_scores = util.cos_sim(query_embedding, corpus_embeddings)[0]
    # Find the top k results
    top_results = torch.topk(cos_scores, k=k)

    results = []
    for score, idx in zip(top_results[0], top_results[1]):
        results.append({
            "Score": round(float(score), 4),
            "Text": corpus[idx]
        })
    return results

# 5. EXECUTION: Testing 5 Example Queries
test_queries = [
    "How can I build a 3D map from 2D images?",
    "What is the best Raga for a beginner?",
    "Where can I find a place to exercise?",
    "Is there help for coding projects?",
    "How many beats in a standard music cycle?"
]

print("\n--- SIMILARITY RESULTS ---")
for q in test_queries:
    print(f"\nQuery: {q}")
    matches = get_top_k(q, k=3)
    for m in matches:
        print(f" -> [{m['Score']}] {m['Text']}")