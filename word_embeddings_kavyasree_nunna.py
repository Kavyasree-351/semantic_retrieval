"""# **The process follows a three-step pipeline:Sentence Embeddings:**

* We use the all-MiniLM-L6-v2 model (a lightweight
Transformer) to map each sentence into a 384-dimensional vector space.In this space, sentences with similar meanings are mathematically positioned close to one another.
* **Cosine Similarity:** To find the "distance" between the user's query and our dataset, we calculate the cosine of the angle between their vectors. A score closer to 1.0 indicates high semantic similarity.
*  **Top-K Retrieval:** The system ranks all sentences based on their similarity scores and returns the top $k$ results.
"""

